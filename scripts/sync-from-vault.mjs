#!/usr/bin/env node
/**
 * sync-from-vault.mjs
 *
 * Copy notes from the parent Obsidian vault into Quartz's content/ directory.
 *
 * A note is published if:
 *   - It does NOT have `publish: false` in frontmatter (always-skip escape hatch), AND
 *   - It has `publish: true` in frontmatter, OR its path matches a pattern in
 *     publish.config.mjs's `publishFolders`.
 *
 * For folder-allowlisted notes, the script injects `publish: true` into the
 * copied frontmatter so Quartz's ExplicitPublish filter accepts them as a
 * second gate.
 *
 * Excalidraw handling:
 *   - Raw `.excalidraw.md` files are NEVER copied.
 *   - Embeds `![[Foo.excalidraw]]` are rewritten to `![[Foo.excalidraw.svg]]`
 *     after copying the exported SVG companion.
 *   - In strict mode (default), missing SVG → build fails with clear message.
 *   - In soft mode (`--soft`), missing SVG → embed replaced with placeholder
 *     and a warning is logged. Lets you preview the pipeline before doing the
 *     one-time bulk export in Obsidian.
 */
import fs from "node:fs/promises"
import path from "node:path"
import url from "node:url"
import matter from "gray-matter"
import { minimatch } from "minimatch"
import publishConfig from "../publish.config.mjs"
import { ensureSvg } from "./excalidraw-to-svg.mjs"

const __filename = url.fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const QUARTZ_ROOT = path.resolve(__dirname, "..")

// Vault location is configurable via publish.config.mjs `vaultRoot` (relative
// to the quartz root or absolute). Env var QUARTZ_VAULT_ROOT overrides for
// one-off runs. Falls back to the quartz folder's parent for backward
// compatibility with the original in-vault layout.
const VAULT_ROOT_RAW =
  process.env.QUARTZ_VAULT_ROOT || publishConfig.vaultRoot || ".."
const VAULT_ROOT = path.resolve(QUARTZ_ROOT, VAULT_ROOT_RAW)
const CONTENT_DIR = path.join(QUARTZ_ROOT, "content")

// In-vault publishing manifest (a Markdown file with YAML frontmatter listing
// what to publish). Configurable via publish.config.mjs `manifestFile`.
// Default: PUBLISH.md at the vault root.
const MANIFEST_FILE = publishConfig.manifestFile || "PUBLISH.md"
const MANIFEST_PATH = path.join(VAULT_ROOT, MANIFEST_FILE)

const SOFT_MODE = process.argv.includes("--soft")

const VAULT_IGNORE = new Set([
  ".git",
  ".github",
  ".obsidian",
  ".trash",
  ".venv",
  ".claude",
  ".claudian",
  "node_modules",
  "quartz",
  "ObsidianNotes",
  "__pycache__",
  "templates",
  "_drafts",
  "_scratch",
  "scratch",
  "drafts",
  "private",
])

const CONTENT_PRESERVE = new Set(["index.md", "about.md", "_static"])

const HARD_BLOCK = (rel) =>
  rel.endsWith(".excalidraw.md") ||
  rel.endsWith(".excalidraw") ||
  rel === MANIFEST_FILE

const stats = {
  notesPublishedViaFlag: 0,
  notesPublishedViaFolder: 0,
  notesExcluded: 0,
  attachmentsCopied: 0,
  excalidrawRewrites: 0,
  excalidrawAutoGenerated: 0,
  excalidrawMissingSvg: [],
  excalidrawGenerateFailures: [],
  brokenWikilinks: 0,
  errors: [],
}

async function exists(p) {
  try {
    await fs.access(p)
    return true
  } catch {
    return false
  }
}

async function walk(dir, base = dir) {
  const out = []
  let entries
  try {
    entries = await fs.readdir(dir, { withFileTypes: true })
  } catch {
    return out
  }
  for (const e of entries) {
    if (VAULT_IGNORE.has(e.name)) continue
    const abs = path.join(dir, e.name)
    if (e.isDirectory()) {
      out.push(...(await walk(abs, base)))
    } else if (e.isFile()) {
      out.push({ abs, rel: path.relative(base, abs) })
    }
  }
  return out
}

async function wipeContent() {
  await fs.mkdir(CONTENT_DIR, { recursive: true })
  const entries = await fs.readdir(CONTENT_DIR, { withFileTypes: true })
  for (const e of entries) {
    if (CONTENT_PRESERVE.has(e.name)) continue
    await fs.rm(path.join(CONTENT_DIR, e.name), {
      recursive: true,
      force: true,
    })
  }
}

async function indexVault(files) {
  const byBasename = new Map()
  const byRelPath = new Map()
  for (const f of files) {
    byRelPath.set(f.rel, f.abs)
    const base = path.basename(f.rel)
    const baseNoExt = base.replace(/\.[^.]+$/, "")
    if (!byBasename.has(baseNoExt)) byBasename.set(baseNoExt, [])
    byBasename.get(baseNoExt).push(f.abs)
    if (!byBasename.has(base)) byBasename.set(base, [])
    byBasename.get(base).push(f.abs)
  }
  return { byBasename, byRelPath }
}

function resolveWikilink(target, index, fromAbs) {
  if (index.byRelPath.has(target)) return index.byRelPath.get(target)
  if (index.byRelPath.has(target + ".md"))
    return index.byRelPath.get(target + ".md")
  const baseName = target.split("/").pop()
  const candidates =
    index.byBasename.get(baseName) ||
    index.byBasename.get(baseName + ".md") ||
    []
  if (candidates.length === 1) return candidates[0]
  if (candidates.length > 1) {
    const fromDir = path.dirname(fromAbs)
    candidates.sort(
      (a, b) =>
        path.relative(fromDir, a).split(path.sep).length -
        path.relative(fromDir, b).split(path.sep).length,
    )
    return candidates[0]
  }
  return null
}

// Locate the sidecar exports next to an Excalidraw source file.
//
// The Obsidian Excalidraw plugin's "Export both dark- and light-themed image"
// setting writes two SVGs (`*.dark.svg` + `*.light.svg`). Older single-theme
// exports left a plain `*.svg` instead. We return the full picture so the
// pipeline can ship the dark/light pair to the post-build step (which emits
// a theme-aware <img> pair), while still degrading gracefully for legacy
// single exports and PNG-only setups.
//
// `primary` is the file actually embedded in the rewritten markdown. The
// post-build step in `fix-excalidraw-paths.mjs` looks for the dark companion
// next to it and upgrades the single <img> into the pair markup when it
// finds one.
async function findExcalidrawSidecars(sourceAbs) {
  const dir = path.dirname(sourceAbs)
  const base = path.basename(sourceAbs).replace(/\.md$/, "")
  const p = (suffix) => path.join(dir, `${base}.${suffix}`)
  const dark = (await exists(p("dark.svg"))) ? p("dark.svg") : null
  const light = (await exists(p("light.svg"))) ? p("light.svg") : null
  const legacySvg = (await exists(p("svg"))) ? p("svg") : null
  const legacyLightPng = (await exists(p("light.png"))) ? p("light.png") : null
  const legacyPng = (await exists(p("png"))) ? p("png") : null

  // Priority for `primary`: light.svg (pair primary) → svg (legacy) → dark.svg
  // (defensive: pair where light export failed) → PNG fallbacks. The pair
  // upgrade in post-build only kicks in when `primary` ends with .light.svg
  // (and the dark companion is present in the built output).
  let primary = null
  if (light) primary = light
  else if (legacySvg) primary = legacySvg
  else if (dark) primary = dark
  else if (legacyLightPng) primary = legacyLightPng
  else if (legacyPng) primary = legacyPng

  return { primary, dark, light }
}

// Back-compat shim retained for any caller expecting the old single-path
// return; the new pipeline uses `findExcalidrawSidecars` directly.
async function findExcalidrawSvg(sourceAbs) {
  const { primary } = await findExcalidrawSidecars(sourceAbs)
  return primary
}

function isExcalidrawTarget(target) {
  return (
    /\.excalidraw(\.md)?$/i.test(target) ||
    /(^|\/)Excalidraw\//i.test(target)
  )
}

async function copyFile(srcAbs, destAbs) {
  await fs.mkdir(path.dirname(destAbs), { recursive: true })
  await fs.copyFile(srcAbs, destAbs)
}

async function loadManifest() {
  if (!(await exists(MANIFEST_PATH))) {
    return { patterns: [], source: "missing" }
  }
  const raw = await fs.readFile(MANIFEST_PATH, "utf8")
  let parsed
  try {
    parsed = matter(raw)
  } catch (e) {
    throw new Error(
      `Could not parse YAML frontmatter in ${MANIFEST_FILE}: ${e.message}`,
    )
  }
  const fm = parsed.data || {}
  // Accept `publish:` (preferred) or legacy aliases `publishFolders:` / `paths:`.
  const list = fm.publish ?? fm.publishFolders ?? fm.paths ?? []
  if (!Array.isArray(list)) {
    throw new Error(
      `'publish' field in ${MANIFEST_FILE} must be a YAML list of glob strings.`,
    )
  }
  const patterns = list
    .map((s) => (typeof s === "string" ? s.trim() : ""))
    .filter((s) => s && !s.startsWith("#"))
  return { patterns, source: "manifest" }
}

let PUBLISH_PATTERNS = []

function matchesPublishFolder(relPath) {
  const posix = relPath.split(path.sep).join("/")
  return PUBLISH_PATTERNS.some((pattern) =>
    minimatch(posix, pattern, { dot: false }),
  )
}

function shouldPublish(relPath, frontmatter) {
  if (frontmatter?.publish === false) return { publish: false, source: null }
  if (frontmatter?.publish === true)
    return { publish: true, source: "flag" }
  if (matchesPublishFolder(relPath))
    return { publish: true, source: "folder" }
  return { publish: false, source: null }
}

// Strip leading `NN-` ordering prefix and convert remaining dashes/underscores
// to spaces so vault filenames like "01-Database-Concepts" render as
// "Database Concepts" in the published site (page titles, explorer, breadcrumbs).
// Preserves the file's own casing.
//
// Examples:
//   "01-Database-Concepts"           → "Database Concepts"
//   "03-gRPC-RPC"                    → "gRPC RPC"
//   "Distributed-Systems-Primitives" → "Distributed Systems Primitives"
//   "index"                          → "index"
function prettifyName(stem) {
  if (!stem || typeof stem !== "string") return stem
  return stem
    .replace(/^\d+[-_]/, "")
    .replace(/[-_]/g, " ")
    .trim()
}

// If the body's first non-empty line is a top-level heading whose text matches
// the title (case-insensitive, ignoring punctuation/whitespace), drop it. This
// removes the duplicate H1 you see when the vault note already has `# Title`
// at the top and the sync injects the same title into frontmatter.
function stripLeadingDuplicateH1(body, title) {
  if (!body || !title) return body
  // Aggressive normalization: lowercase, strip whitespace/dashes/punctuation,
  // and drop short connecting words ("and", "or", "vs", "the") so a body
  // heading like "TCP, UDP, and QUIC" still matches a synced title of
  // "TCP UDP QUIC" derived from the filename.
  const norm = (s) =>
    String(s)
      .toLowerCase()
      .replace(/\b(and|or|vs|versus|the|a|an)\b/g, "")
      .replace(/[^a-z0-9]+/g, "")
  const titleNorm = norm(title)
  if (!titleNorm) return body
  const m = body.match(/^\s*#\s+(.+?)\s*\n+/)
  if (!m) return body
  if (norm(m[1]) !== titleNorm) return body
  return body.slice(m[0].length).replace(/^\s+/, "")
}

async function processNote(file, index) {
  const { abs, rel } = file
  if (HARD_BLOCK(rel)) return

  const raw = await fs.readFile(abs, "utf8")
  let parsed
  try {
    parsed = matter(raw)
  } catch (e) {
    stats.errors.push(`Frontmatter parse failed in ${rel}: ${e.message}`)
    return
  }

  const decision = shouldPublish(rel, parsed.data)
  if (!decision.publish) {
    stats.notesExcluded++
    return
  }

  let body = parsed.content
  const data = { ...parsed.data }
  data.publish = true

  // Title resolution (highest priority first):
  //   1. Explicit `title:` in the vault note's frontmatter — honored as-is.
  //   2. The body's first H1 (`# Foo`) — captured as the display title and
  //      then removed from the body so it doesn't render twice.
  //   3. Prettified filename stem — strip leading `NN-` ordering prefix and
  //      replace dashes with spaces. For `index.md`, use the parent folder
  //      name so breadcrumbs/explorer show "Distributed Systems" instead of
  //      "index".
  // This means the vault stays untouched: notes that already follow the
  // Obsidian convention of `# Heading\n\n...content...` just work.
  const stemFromRel = (relPath) => {
    const base = path.basename(relPath, path.extname(relPath))
    if (base === "index") {
      const parent = path.basename(path.dirname(relPath))
      return parent && parent !== "." ? parent : "index"
    }
    return base
  }
  const hasExplicitTitle =
    data.title != null && data.title !== "" && data.title !== "index"
  if (!hasExplicitTitle) {
    const leadingH1 = body.match(/^\s*#\s+(.+?)\s*\n+/)
    if (leadingH1) {
      data.title = leadingH1[1].trim()
      body = body.slice(leadingH1[0].length).replace(/^\s+/, "")
    } else {
      data.title = prettifyName(stemFromRel(rel))
    }
  } else {
    // Even when the user set an explicit title, strip a duplicate leading H1
    // so the page never shows two identical headings.
    body = stripLeadingDuplicateH1(body, data.title)
  }

  const embedRe = /!\[\[([^\]|#]+)(#[^\]|]+)?(?:\|([^\]]+))?\]\]/g
  const linkRe = /(?<!\!)\[\[([^\]|#]+)(#[^\]|]+)?(?:\|([^\]]+))?\]\]/g

  const rewrites = []
  const seenTargets = new Set()
  let m
  while ((m = embedRe.exec(body)) !== null) {
    const fullMatch = m[0]
    const target = m[1].trim()
    const alias = m[3] ? `|${m[3]}` : ""
    if (seenTargets.has(fullMatch)) continue
    seenTargets.add(fullMatch)

    let resolved = resolveWikilink(target, index, abs)

    if (isExcalidrawTarget(target)) {
      let sourceAbs = resolved
      if (!sourceAbs || !sourceAbs.endsWith(".md")) {
        const tryName = target.endsWith(".excalidraw")
          ? target + ".md"
          : target.endsWith(".excalidraw.md")
            ? target
            : target + ".excalidraw.md"
        sourceAbs = resolveWikilink(tryName, index, abs)
      }
      if (!sourceAbs) {
        const msg = `Excalidraw embed '${target}' in ${rel} could not be resolved.`
        if (SOFT_MODE) {
          stats.excalidrawMissingSvg.push(`${rel}: ${target} (unresolved)`)
          rewrites.push({
            from: fullMatch,
            to: `*[Excalidraw drawing \`${target}\` — unresolved]*`,
          })
        } else {
          stats.errors.push(msg)
        }
        continue
      }
      let sidecars = await findExcalidrawSidecars(sourceAbs)
      let svgAbs = sidecars.primary
      if (!svgAbs && sourceAbs.endsWith(".excalidraw.md")) {
        try {
          svgAbs = await ensureSvg(sourceAbs)
          stats.excalidrawAutoGenerated++
        } catch (e) {
          stats.excalidrawGenerateFailures.push(
            `${path.basename(sourceAbs)}: ${e.message}`,
          )
        }
      }
      if (!svgAbs) {
        if (SOFT_MODE) {
          stats.excalidrawMissingSvg.push(`${rel}: ${target}`)
          rewrites.push({
            from: fullMatch,
            to: `*[Excalidraw drawing \`${target}\` — SVG export pending]*`,
          })
        } else {
          stats.errors.push(
            `Excalidraw embed '${target}' in ${rel} has no exported SVG/PNG companion and auto-generation failed.\n` +
              `  → See "Excalidraw build fails" details above for the underlying error.\n` +
              `  → Or use 'npm run sync -- --soft' to preview without this drawing.`,
          )
        }
        continue
      }
      // Copy the primary file plus any dark/light companion(s) so Quartz's
      // asset emitter ships all variants. The post-build step then upgrades
      // the single <img> to a theme-aware pair when both are present.
      const toCopy = new Set([svgAbs])
      if (sidecars.dark) toCopy.add(sidecars.dark)
      if (sidecars.light) toCopy.add(sidecars.light)
      for (const srcAbs of toCopy) {
        const vaultRel = path.relative(VAULT_ROOT, srcAbs)
        await copyFile(srcAbs, path.join(CONTENT_DIR, vaultRel))
        stats.attachmentsCopied++
      }
      const svgVaultRel = path.relative(VAULT_ROOT, svgAbs)
      const svgDest = path.join(CONTENT_DIR, svgVaultRel)
      // Use a markdown image with a relative URL from the note's own
      // location to the SVG. Wikilinks aren't reliably resolved by Quartz
      // for binary assets sitting in a sibling directory, and absolute
      // ("/Excalidraw/...") URLs break under GitHub Pages project sites.
      const noteDirAbs = path.dirname(path.join(CONTENT_DIR, rel))
      let relUrl = path.relative(noteDirAbs, svgDest).split(path.sep).join("/")
      if (!relUrl.startsWith(".") && !relUrl.startsWith("/")) {
        relUrl = "./" + relUrl
      }
      const altText = m[3]
        ? m[3]
        : path
            .basename(svgAbs)
            .replace(/\.(dark|light)\.(svg|png)$/, "")
            .replace(/\.(svg|png)$/, "")
      rewrites.push({
        from: fullMatch,
        to: `![${altText}](${encodeURI(relUrl)})`,
      })
      stats.excalidrawRewrites++
      continue
    }

    if (resolved) {
      const isMarkdown = resolved.endsWith(".md")
      if (!isMarkdown) {
        const attRel = path.relative(VAULT_ROOT, resolved)
        const attDest = path.join(CONTENT_DIR, attRel)
        await copyFile(resolved, attDest)
        stats.attachmentsCopied++
      }
    } else {
      stats.brokenWikilinks++
    }
  }

  for (const { from, to } of rewrites) {
    body = body.split(from).join(to)
  }

  while ((m = linkRe.exec(body)) !== null) {
    const target = m[1].trim()
    if (!resolveWikilink(target, index, abs)) stats.brokenWikilinks++
  }

  const outAbs = path.join(CONTENT_DIR, rel)
  await fs.mkdir(path.dirname(outAbs), { recursive: true })
  await fs.writeFile(outAbs, matter.stringify(body, data), "utf8")

  if (decision.source === "flag") stats.notesPublishedViaFlag++
  else stats.notesPublishedViaFolder++
}

async function main() {
  if (!(await exists(VAULT_ROOT))) {
    console.error(
      `\n❌ Vault path does not exist: ${VAULT_ROOT}\n` +
        `   Set 'vaultRoot' in publish.config.mjs (relative to quartz root or absolute),\n` +
        `   or export QUARTZ_VAULT_ROOT=/absolute/path/to/vault for a one-off run.`,
    )
    process.exit(1)
  }
  if (path.resolve(VAULT_ROOT) === path.resolve(QUARTZ_ROOT)) {
    console.error(
      `\n❌ vaultRoot resolves to the quartz folder itself (${VAULT_ROOT}).\n` +
        `   The vault must be a separate directory.`,
    )
    process.exit(1)
  }

  let manifest
  try {
    manifest = await loadManifest()
  } catch (e) {
    console.error(`\n❌ ${e.message}`)
    process.exit(1)
  }
  if (manifest.source === "missing") {
    console.error(
      `\n❌ Publishing manifest not found at ${MANIFEST_PATH}\n` +
        `   Create it in your vault with YAML frontmatter, e.g.:\n` +
        `   ---\n` +
        `   publish:\n` +
        `     - 01-Fundamentals/01-Concepts/**\n` +
        `   ---`,
    )
    process.exit(1)
  }
  PUBLISH_PATTERNS = manifest.patterns

  console.log(`📚 Vault:    ${VAULT_ROOT}`)
  console.log(`📝 Manifest: ${MANIFEST_PATH}`)
  console.log(`🌱 Content:  ${CONTENT_DIR}`)
  console.log(`⚙️  Publish:  ${JSON.stringify(PUBLISH_PATTERNS)}`)
  if (SOFT_MODE) console.log(`🧪 SOFT MODE: Excalidraw failures will warn, not fail.`)
  console.log()

  await wipeContent()

  const allFiles = await walk(VAULT_ROOT)
  const markdown = allFiles.filter((f) => f.rel.endsWith(".md"))
  const index = await indexVault(allFiles)

  for (const f of markdown) {
    try {
      await processNote(f, index)
    } catch (e) {
      stats.errors.push(`${f.rel}: ${e.message}`)
    }
  }

  const totalPublished =
    stats.notesPublishedViaFlag + stats.notesPublishedViaFolder
  console.log(`\n── sync summary ─────────────────────────`)
  console.log(`  Published (flag):       ${stats.notesPublishedViaFlag}`)
  console.log(`  Published (folder):     ${stats.notesPublishedViaFolder}`)
  console.log(`  Excluded:               ${stats.notesExcluded}`)
  console.log(`  Attachments copied:     ${stats.attachmentsCopied}`)
  console.log(`  Excalidraw rewrites:    ${stats.excalidrawRewrites}`)
  console.log(`  Excalidraw auto-built:  ${stats.excalidrawAutoGenerated}`)
  console.log(`  Excalidraw build fails: ${stats.excalidrawGenerateFailures.length}`)
  console.log(`  Excalidraw missing SVG: ${stats.excalidrawMissingSvg.length}`)
  console.log(`  Unresolved wikilinks:   ${stats.brokenWikilinks}`)
  console.log(`  Errors:                 ${stats.errors.length}`)

  if (stats.excalidrawGenerateFailures.length > 0) {
    console.warn(
      `\n⚠  ${stats.excalidrawGenerateFailures.length} Excalidraw drawing(s) could not be auto-rendered:`,
    )
    for (const m of stats.excalidrawGenerateFailures.slice(0, 10)) {
      console.warn(`     - ${m}`)
    }
    if (stats.excalidrawGenerateFailures.length > 10) {
      console.warn(
        `     ... and ${stats.excalidrawGenerateFailures.length - 10} more`,
      )
    }
  }

  if (stats.excalidrawMissingSvg.length > 0 && SOFT_MODE) {
    console.warn(
      `\n⚠  ${stats.excalidrawMissingSvg.length} Excalidraw drawing(s) have no exported SVG.`,
    )
    console.warn(`   First few:`)
    for (const m of stats.excalidrawMissingSvg.slice(0, 10)) {
      console.warn(`     - ${m}`)
    }
    if (stats.excalidrawMissingSvg.length > 10) {
      console.warn(`     ... and ${stats.excalidrawMissingSvg.length - 10} more`)
    }
    console.warn(
      `\n   To fix: in Obsidian → Settings → Excalidraw → enable "Auto-export SVG"` +
        ` + "Keep in sync", then run the command\n   "Excalidraw: Re-export SVG/PNG for every Excalidraw file".`,
    )
  }

  if (stats.errors.length) {
    console.error("\n❌ Errors:")
    for (const e of stats.errors.slice(0, 20)) console.error(`  - ${e}`)
    if (stats.errors.length > 20)
      console.error(`  ... and ${stats.errors.length - 20} more`)
    process.exit(1)
  }
  if (totalPublished === 0) {
    console.error(
      "\n❌ No notes were published. Add `publish: true` to a note, or expand publishFolders in publish.config.mjs.",
    )
    process.exit(1)
  }
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
