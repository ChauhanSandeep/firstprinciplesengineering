#!/usr/bin/env node
/**
 * sync-from-vault.mjs
 *
 * Copy notes from the parent Obsidian vault into Quartz's content/ directory.
 *
 * A note is published if:
 *   - It does NOT have `publish: false` in frontmatter (always-skip escape hatch), AND
 *   - It has `publish: true` in frontmatter, OR its path matches a pattern in
 *     publish.config.mjs's `publishFolders`.
 *
 * For folder-allowlisted notes, the script injects `publish: true` into the
 * copied frontmatter so Quartz's ExplicitPublish filter accepts them as a
 * second gate.
 *
 * Excalidraw handling:
 *   - Raw `.excalidraw.md` files are NEVER copied.
 *   - Embeds `![[Foo.excalidraw]]` are rewritten to `![[Foo.excalidraw.svg]]`
 *     after copying the exported SVG companion.
 *   - In strict mode (default), missing SVG → build fails with clear message.
 *   - In soft mode (`--soft`), missing SVG → embed replaced with placeholder
 *     and a warning is logged. Lets you preview the pipeline before doing the
 *     one-time bulk export in Obsidian.
 */
import fs from "node:fs/promises"
import path from "node:path"
import url from "node:url"
import matter from "gray-matter"
import { minimatch } from "minimatch"
import publishConfig from "../publish.config.mjs"
import { ensureSvg } from "./excalidraw-to-svg.mjs"

const __filename = url.fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const QUARTZ_ROOT = path.resolve(__dirname, "..")
const VAULT_ROOT = path.resolve(QUARTZ_ROOT, "..")
const CONTENT_DIR = path.join(QUARTZ_ROOT, "content")

const SOFT_MODE = process.argv.includes("--soft")

const VAULT_IGNORE = new Set([
  ".git",
  ".github",
  ".obsidian",
  ".trash",
  ".venv",
  ".claude",
  ".claudian",
  "node_modules",
  "quartz",
  "ObsidianNotes",
  "__pycache__",
])

const CONTENT_PRESERVE = new Set(["index.md", "_static"])

const HARD_BLOCK = (rel) =>
  rel.endsWith(".excalidraw.md") || rel.endsWith(".excalidraw")

const stats = {
  notesPublishedViaFlag: 0,
  notesPublishedViaFolder: 0,
  notesExcluded: 0,
  attachmentsCopied: 0,
  excalidrawRewrites: 0,
  excalidrawAutoGenerated: 0,
  excalidrawMissingSvg: [],
  excalidrawGenerateFailures: [],
  brokenWikilinks: 0,
  errors: [],
}

async function exists(p) {
  try {
    await fs.access(p)
    return true
  } catch {
    return false
  }
}

async function walk(dir, base = dir) {
  const out = []
  let entries
  try {
    entries = await fs.readdir(dir, { withFileTypes: true })
  } catch {
    return out
  }
  for (const e of entries) {
    if (VAULT_IGNORE.has(e.name)) continue
    const abs = path.join(dir, e.name)
    if (e.isDirectory()) {
      out.push(...(await walk(abs, base)))
    } else if (e.isFile()) {
      out.push({ abs, rel: path.relative(base, abs) })
    }
  }
  return out
}

async function wipeContent() {
  await fs.mkdir(CONTENT_DIR, { recursive: true })
  const entries = await fs.readdir(CONTENT_DIR, { withFileTypes: true })
  for (const e of entries) {
    if (CONTENT_PRESERVE.has(e.name)) continue
    await fs.rm(path.join(CONTENT_DIR, e.name), {
      recursive: true,
      force: true,
    })
  }
}

async function indexVault(files) {
  const byBasename = new Map()
  const byRelPath = new Map()
  for (const f of files) {
    byRelPath.set(f.rel, f.abs)
    const base = path.basename(f.rel)
    const baseNoExt = base.replace(/\.[^.]+$/, "")
    if (!byBasename.has(baseNoExt)) byBasename.set(baseNoExt, [])
    byBasename.get(baseNoExt).push(f.abs)
    if (!byBasename.has(base)) byBasename.set(base, [])
    byBasename.get(base).push(f.abs)
  }
  return { byBasename, byRelPath }
}

function resolveWikilink(target, index, fromAbs) {
  if (index.byRelPath.has(target)) return index.byRelPath.get(target)
  if (index.byRelPath.has(target + ".md"))
    return index.byRelPath.get(target + ".md")
  const baseName = target.split("/").pop()
  const candidates =
    index.byBasename.get(baseName) ||
    index.byBasename.get(baseName + ".md") ||
    []
  if (candidates.length === 1) return candidates[0]
  if (candidates.length > 1) {
    const fromDir = path.dirname(fromAbs)
    candidates.sort(
      (a, b) =>
        path.relative(fromDir, a).split(path.sep).length -
        path.relative(fromDir, b).split(path.sep).length,
    )
    return candidates[0]
  }
  return null
}

async function findExcalidrawSvg(sourceAbs) {
  const dir = path.dirname(sourceAbs)
  const base = path.basename(sourceAbs).replace(/\.md$/, "")
  const candidates = [
    path.join(dir, `${base}.svg`),
    path.join(dir, `${base}.light.svg`),
    path.join(dir, `${base}.png`),
    path.join(dir, `${base}.light.png`),
  ]
  for (const c of candidates) {
    if (await exists(c)) return c
  }
  return null
}

function isExcalidrawTarget(target) {
  return (
    /\.excalidraw(\.md)?$/i.test(target) ||
    /(^|\/)Excalidraw\//i.test(target)
  )
}

async function copyFile(srcAbs, destAbs) {
  await fs.mkdir(path.dirname(destAbs), { recursive: true })
  await fs.copyFile(srcAbs, destAbs)
}

function matchesPublishFolder(relPath) {
  const posix = relPath.split(path.sep).join("/")
  return (publishConfig.publishFolders || []).some((pattern) =>
    minimatch(posix, pattern, { dot: false }),
  )
}

function shouldPublish(relPath, frontmatter) {
  if (frontmatter?.publish === false) return { publish: false, source: null }
  if (frontmatter?.publish === true)
    return { publish: true, source: "flag" }
  if (matchesPublishFolder(relPath))
    return { publish: true, source: "folder" }
  return { publish: false, source: null }
}

async function processNote(file, index) {
  const { abs, rel } = file
  if (HARD_BLOCK(rel)) return

  const raw = await fs.readFile(abs, "utf8")
  let parsed
  try {
    parsed = matter(raw)
  } catch (e) {
    stats.errors.push(`Frontmatter parse failed in ${rel}: ${e.message}`)
    return
  }

  const decision = shouldPublish(rel, parsed.data)
  if (!decision.publish) {
    stats.notesExcluded++
    return
  }

  let body = parsed.content
  const data = { ...parsed.data }
  data.publish = true

  const embedRe = /!\[\[([^\]|#]+)(#[^\]|]+)?(?:\|([^\]]+))?\]\]/g
  const linkRe = /(?<!\!)\[\[([^\]|#]+)(#[^\]|]+)?(?:\|([^\]]+))?\]\]/g

  const rewrites = []
  const seenTargets = new Set()
  let m
  while ((m = embedRe.exec(body)) !== null) {
    const fullMatch = m[0]
    const target = m[1].trim()
    const alias = m[3] ? `|${m[3]}` : ""
    if (seenTargets.has(fullMatch)) continue
    seenTargets.add(fullMatch)

    let resolved = resolveWikilink(target, index, abs)

    if (isExcalidrawTarget(target)) {
      let sourceAbs = resolved
      if (!sourceAbs || !sourceAbs.endsWith(".md")) {
        const tryName = target.endsWith(".excalidraw")
          ? target + ".md"
          : target.endsWith(".excalidraw.md")
            ? target
            : target + ".excalidraw.md"
        sourceAbs = resolveWikilink(tryName, index, abs)
      }
      if (!sourceAbs) {
        const msg = `Excalidraw embed '${target}' in ${rel} could not be resolved.`
        if (SOFT_MODE) {
          stats.excalidrawMissingSvg.push(`${rel}: ${target} (unresolved)`)
          rewrites.push({
            from: fullMatch,
            to: `*[Excalidraw drawing \`${target}\` — unresolved]*`,
          })
        } else {
          stats.errors.push(msg)
        }
        continue
      }
      let svgAbs = await findExcalidrawSvg(sourceAbs)
      if (!svgAbs && sourceAbs.endsWith(".excalidraw.md")) {
        try {
          svgAbs = await ensureSvg(sourceAbs)
          stats.excalidrawAutoGenerated++
        } catch (e) {
          stats.excalidrawGenerateFailures.push(
            `${path.basename(sourceAbs)}: ${e.message}`,
          )
        }
      }
      if (!svgAbs) {
        if (SOFT_MODE) {
          stats.excalidrawMissingSvg.push(`${rel}: ${target}`)
          rewrites.push({
            from: fullMatch,
            to: `*[Excalidraw drawing \`${target}\` — SVG export pending]*`,
          })
        } else {
          stats.errors.push(
            `Excalidraw embed '${target}' in ${rel} has no exported SVG/PNG companion and auto-generation failed.\n` +
              `  → See "Excalidraw build fails" details above for the underlying error.\n` +
              `  → Or use 'npm run sync -- --soft' to preview without this drawing.`,
          )
        }
        continue
      }
      const svgVaultRel = path.relative(VAULT_ROOT, svgAbs)
      const svgDest = path.join(CONTENT_DIR, svgVaultRel)
      await copyFile(svgAbs, svgDest)
      stats.attachmentsCopied++
      // Use a markdown image with a relative URL from the note's own
      // location to the SVG. Wikilinks aren't reliably resolved by Quartz
      // for binary assets sitting in a sibling directory, and absolute
      // ("/Excalidraw/...") URLs break under GitHub Pages project sites.
      const noteDirAbs = path.dirname(path.join(CONTENT_DIR, rel))
      let relUrl = path.relative(noteDirAbs, svgDest).split(path.sep).join("/")
      if (!relUrl.startsWith(".") && !relUrl.startsWith("/")) {
        relUrl = "./" + relUrl
      }
      const altText = m[3] ? m[3] : path.basename(svgAbs, ".svg")
      rewrites.push({
        from: fullMatch,
        to: `![${altText}](${encodeURI(relUrl)})`,
      })
      stats.excalidrawRewrites++
      continue
    }

    if (resolved) {
      const isMarkdown = resolved.endsWith(".md")
      if (!isMarkdown) {
        const attRel = path.relative(VAULT_ROOT, resolved)
        const attDest = path.join(CONTENT_DIR, attRel)
        await copyFile(resolved, attDest)
        stats.attachmentsCopied++
      }
    } else {
      stats.brokenWikilinks++
    }
  }

  for (const { from, to } of rewrites) {
    body = body.split(from).join(to)
  }

  while ((m = linkRe.exec(body)) !== null) {
    const target = m[1].trim()
    if (!resolveWikilink(target, index, abs)) stats.brokenWikilinks++
  }

  const outAbs = path.join(CONTENT_DIR, rel)
  await fs.mkdir(path.dirname(outAbs), { recursive: true })
  await fs.writeFile(outAbs, matter.stringify(body, data), "utf8")

  if (decision.source === "flag") stats.notesPublishedViaFlag++
  else stats.notesPublishedViaFolder++
}

async function main() {
  console.log(`📚 Vault:   ${VAULT_ROOT}`)
  console.log(`🌱 Content: ${CONTENT_DIR}`)
  console.log(`⚙️  Folders: ${JSON.stringify(publishConfig.publishFolders)}`)
  if (SOFT_MODE) console.log(`🧪 SOFT MODE: Excalidraw failures will warn, not fail.`)
  console.log()

  await wipeContent()

  const allFiles = await walk(VAULT_ROOT)
  const markdown = allFiles.filter((f) => f.rel.endsWith(".md"))
  const index = await indexVault(allFiles)

  for (const f of markdown) {
    try {
      await processNote(f, index)
    } catch (e) {
      stats.errors.push(`${f.rel}: ${e.message}`)
    }
  }

  const totalPublished =
    stats.notesPublishedViaFlag + stats.notesPublishedViaFolder
  console.log(`\n── sync summary ─────────────────────────`)
  console.log(`  Published (flag):       ${stats.notesPublishedViaFlag}`)
  console.log(`  Published (folder):     ${stats.notesPublishedViaFolder}`)
  console.log(`  Excluded:               ${stats.notesExcluded}`)
  console.log(`  Attachments copied:     ${stats.attachmentsCopied}`)
  console.log(`  Excalidraw rewrites:    ${stats.excalidrawRewrites}`)
  console.log(`  Excalidraw auto-built:  ${stats.excalidrawAutoGenerated}`)
  console.log(`  Excalidraw build fails: ${stats.excalidrawGenerateFailures.length}`)
  console.log(`  Excalidraw missing SVG: ${stats.excalidrawMissingSvg.length}`)
  console.log(`  Unresolved wikilinks:   ${stats.brokenWikilinks}`)
  console.log(`  Errors:                 ${stats.errors.length}`)

  if (stats.excalidrawGenerateFailures.length > 0) {
    console.warn(
      `\n⚠  ${stats.excalidrawGenerateFailures.length} Excalidraw drawing(s) could not be auto-rendered:`,
    )
    for (const m of stats.excalidrawGenerateFailures.slice(0, 10)) {
      console.warn(`     - ${m}`)
    }
    if (stats.excalidrawGenerateFailures.length > 10) {
      console.warn(
        `     ... and ${stats.excalidrawGenerateFailures.length - 10} more`,
      )
    }
  }

  if (stats.excalidrawMissingSvg.length > 0 && SOFT_MODE) {
    console.warn(
      `\n⚠  ${stats.excalidrawMissingSvg.length} Excalidraw drawing(s) have no exported SVG.`,
    )
    console.warn(`   First few:`)
    for (const m of stats.excalidrawMissingSvg.slice(0, 10)) {
      console.warn(`     - ${m}`)
    }
    if (stats.excalidrawMissingSvg.length > 10) {
      console.warn(`     ... and ${stats.excalidrawMissingSvg.length - 10} more`)
    }
    console.warn(
      `\n   To fix: in Obsidian → Settings → Excalidraw → enable "Auto-export SVG"` +
        ` + "Keep in sync", then run the command\n   "Excalidraw: Re-export SVG/PNG for every Excalidraw file".`,
    )
  }

  if (stats.errors.length) {
    console.error("\n❌ Errors:")
    for (const e of stats.errors.slice(0, 20)) console.error(`  - ${e}`)
    if (stats.errors.length > 20)
      console.error(`  ... and ${stats.errors.length - 20} more`)
    process.exit(1)
  }
  if (totalPublished === 0) {
    console.error(
      "\n❌ No notes were published. Add `publish: true` to a note, or expand publishFolders in publish.config.mjs.",
    )
    process.exit(1)
  }
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
